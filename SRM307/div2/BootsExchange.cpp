    int leastAmount(vector <int> left, vector <int> right) {
		int n = left.size();
		map<int, int> a, b;
		set<int> st;
		rep(i, n) {
			a[left[i]]++;
			b[right[i]]++;
			st.insert(left[i]);
			st.insert(right[i]);
		}

		int cnt = 0;
		each(it, st) {
			int x = a[*it];
			int y = b[*it];

			if(x < y) swap(x, y);
			cnt += (x - y);
		}

		return cnt / 2;
    }

    
